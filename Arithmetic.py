"""
The function adds two numbers
"""


def add(num1, num2):
    result = num1 + num2
    return result


"""
The function subtracts two numbers
"""


def sub(num1, num2):
    result = num1 - num2
    return result


"""
The function multiplies two numbers
"""


def mul(num1, num2):
    result = num1 * num2
    return result


"""
The function divides two numbers
"""


def div(num1, num2):
    result = num1 / num2
    return result
